package fr.soat.springdata.neo4j.sdneo4j.modele;

public class Acteur {

	private String nom, prenom;

	public String getNom() {
		return this.nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return this.prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
}
